def getAmbientFromStr(str):
    if not str:
        return None
    if 'jungle' in str:
        return AMBIENT_JUNGLE
    elif 'cave' in str:
        return AMBIENT_CAVE
    elif 'volcano' in str:
        return AMBIENT_VOLCANO
    elif 'swamp' in str:
        return AMBIENT_SWAMP
    elif 'spanish' in str:
        return AMBIENT_SPANISH
    elif 'shanty' in str:
        return AMBIENT_SHANTY
    elif 'jail_interior' in str:
        return AMBIENT_JAIL
    return None


